// Задача 10: Напишите программу, которая принимает на вход трёхзначное число и на выходе показывает вторую цифру этого числа.
// 456 -> 5
// 782 -> 8
// 918 -> 1


using System;

class Program
{
    static void Main(string[] args)
    {
        Console.Write("Введите трехзначное число: ");//Вывод на консоль сообщения "Введите трехзначное число:"
        int number = int.Parse(Console.ReadLine());//Считывание введенного числа типа int в переменную number.

        if (number >= 100 && number <=999)//Проверка на то, что введенное число является трехзначным и находится в диапазоне от 100 до 999.
        {   
            int secondDigit = (number / 10) % 10;//Если число соответствует условиям, то нахождение второй цифры в числе и присваивание ее в переменную secondDigit.
            Console.WriteLine($"Вторая цифра числа {number} равна {secondDigit}.");//Вывод на консоль сообщения, которое содержит исходное число и его вторую цифру.
        }
        else 
        {
            System.Console.WriteLine("Некорректное число!");//Если введенное число не соответствует требованиям, то выводится сообщение "Некорректное число!".
        }

        Console.ReadLine();
    }
}
